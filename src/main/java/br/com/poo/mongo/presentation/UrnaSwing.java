    private void testarIndiceNumeroCandidato(String auxNumCandidato) {
        if (txtNumero.getText().equals("")) {
            tocarSom("SomTecla");
            txtNumero.setText(auxNumCandidato);
            numeroCandidato.append(auxNumCandidato);
            return;
        }
        if (txtNumero1.getText().equals("")) {
            tocarSom("SomTecla");
            txtNumero1.setText(auxNumCandidato);
            numeroCandidato.append(auxNumCandidato);
            lblNumero.setVisible(true);
            lblPartido.setVisible(true);
            imprimirPartido();
            jInfoConfirma.setVisible(true);
            return;
        }
        if (txtNumero2.getText().equals("")) {
            tocarSom("SomTecla");
            txtNumero2.setText(auxNumCandidato);
            numeroCandidato.append(auxNumCandidato);
            return;
        }
        if (txtNumero3.getText().equals("")) {
            tocarSom("SomTecla");
            txtNumero3.setText(auxNumCandidato);
            numeroCandidato.append(auxNumCandidato);
            return;
        }
        if (txtNumero4.getText().equals("")) {
            tocarSom("SomTecla");
            txtNumero4.setText(auxNumCandidato);
            numeroCandidato.append(auxNumCandidato);
            try {
                if (service.getInfoCandidatos(Integer.parseInt(numeroCandidato.toString())) != null) {
                    imprimirCandidato();
                    lblNome.setVisible(true);
                }
            } catch (CandidatoInexistenteExcepition ex) {
                imprimirCandidato();
            }

        }

    }

    private class FinalizarVoto extends Thread {

        public void run() {

            setVisibleFinal();
            jProgressBar.setMaximum(100);
            do {
                jProgressBar.setValue(jProgressBar.getValue() + 1);
                jProgressBar.setStringPainted(true);
                try {
                    if (jProgressBar.getValue() < 20 || (jProgressBar.getValue() < 85 && jProgressBar.getValue() >= 50)) {
                        sleep(10);
                    }
                    if (jProgressBar.getValue() >= 70 || (jProgressBar.getValue() >= 20 && jProgressBar.getValue() < 50)) {
                        sleep(2);
                    }

                } catch (InterruptedException ex) {
                }
            } while (jProgressBar.getValue() < jProgressBar.getMaximum());
            tocarSom("fim");
            lblFim.setVisible(true);
            jProgressBar.setVisible(false);
            try {
                sleep(2000);
            } catch (InterruptedException ex) {
                Logger.getLogger(UrnaSwing.class.getName()).log(Level.SEVERE, null, ex);
            }
            corrigir();
            iniciarVotacao();
            repaint();

        }

    }

    
    private class ImprimirResultado extends Thread {

        public void run() {

            setVisibleFinal();
            jProgressBar.setMaximum(1000);
            do {
                jProgressBar.setValue(jProgressBar.getValue() + 1);
                jProgressBar.setStringPainted(true);
                if(jProgressBar.getValue() < 250){
                    lblCarregando.setText("Carregando......");
                }
                else if(jProgressBar.getValue() >= 250 && jProgressBar.getValue() < 750){
                    lblCarregando.setText("Calculando os votos......");
                }
                else{
                    lblCarregando.setText("Fechando conexo com o banco de dados......");
                }
                
                try {
                    if (jProgressBar.getValue() < 200 || (jProgressBar.getValue() < 850 && jProgressBar.getValue() >= 500)) {
                        sleep(9);
                        
                    }
                    if (jProgressBar.getValue() >= 700 || (jProgressBar.getValue() >= 200 && jProgressBar.getValue() < 500)) {
                        sleep(3);
                        
                    }

                } catch (InterruptedException ex) {
                }
            } while (jProgressBar.getValue() < jProgressBar.getMaximum());
            
            try {
                lblCarregando.setText("Imprimindo......");
                sleep(2000);
            } catch (InterruptedException ex) {
                Logger.getLogger(UrnaSwing.class.getName()).log(Level.SEVERE, null, ex);
            }
            criarListCandidatos();
            mostrarResultadoFinal();

        }

    }
    
